import fs from "node:fs/promises";
import path from "node:path";

const auditPath =
  process.argv[2] ||
  path.join(
    process.cwd(),
    "outputs",
    "catalog-audit-2026-06-28",
    "previous-audit.json"
  );
const outputPath =
  process.argv[3] ||
  path.join(process.cwd(), "lib", "catalog-visibility.generated.mjs");

const audit = JSON.parse(await fs.readFile(auditPath, "utf8"));
const rows = Array.isArray(audit) ? audit : audit.rows || [];
const decisions = rows
  .filter((row) =>
    ["hide", "remove"].includes(String(row.Recommendation || "").toLowerCase())
  )
  .map((row) => ({
    id: Number(row["Product ID"]),
    name: String(row.Product || ""),
    decision: String(row.Recommendation || "").toLowerCase(),
    reason: String(row.Reason || ""),
  }))
  .filter((row) => Number.isInteger(row.id))
  .sort((left, right) => left.id - right.id);

const hidden = decisions.filter((row) => row.decision === "hide");
const removed = decisions.filter((row) => row.decision === "remove");
const serialize = (value) => JSON.stringify(value, null, 2);

const source = `// Generated by scripts/generate-catalog-visibility.mjs.
// Public discovery excludes these reviewed legacy, incumbent, invalid, duplicate,
// and inactive catalog records. The underlying Supabase rows remain untouched.

export const HIDDEN_CATALOG_PRODUCTS = Object.freeze(${serialize(hidden)});
export const REMOVED_CATALOG_PRODUCTS = Object.freeze(${serialize(removed)});
export const EXCLUDED_CATALOG_PRODUCT_IDS = Object.freeze([
  ...HIDDEN_CATALOG_PRODUCTS.map((product) => product.id),
  ...REMOVED_CATALOG_PRODUCTS.map((product) => product.id),
]);
`;

await fs.writeFile(outputPath, source);
console.log(
  JSON.stringify({
    outputPath,
    hidden: hidden.length,
    removed: removed.length,
    excluded: decisions.length,
  })
);
